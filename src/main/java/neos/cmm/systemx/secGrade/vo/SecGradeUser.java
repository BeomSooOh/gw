package neos.cmm.systemx.secGrade.vo;

public class SecGradeUser {
	
	private String secId;
	private String deptSeq;
	private String empSeq;
	private String dpNamePosition;
	private String deptName;
	private String empName;
	public String getSecId() {
		return secId;
	}
	public void setSecId(String secId) {
		this.secId = secId;
	}
	public String getDeptSeq() {
		return deptSeq;
	}
	public void setDeptSeq(String deptSeq) {
		this.deptSeq = deptSeq;
	}
	public String getEmpSeq() {
		return empSeq;
	}
	public void setEmpSeq(String empSeq) {
		this.empSeq = empSeq;
	}
	public String getDpNamePosition() {
		return dpNamePosition;
	}
	public void setDpNamePosition(String dpNamePosition) {
		this.dpNamePosition = dpNamePosition;
	}
	public String getEmpName() {
		return empName;
	}
	public void setEmpName(String empName) {
		this.empName = empName;
	}
	public String getDeptName() {
		return deptName;
	}
	public void setDeptName(String deptName) {
		this.deptName = deptName;
	}
	
}
