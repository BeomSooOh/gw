public class SecGradeRemoveResponse {
	private String canRemoveYn;
	private String type;
	private String typeMessage;
	private String callBackYn;
	private String callBackUrl;
	public String getCanRemoveYn() {
		return canRemoveYn;
	}
	public void setCanRemoveYn(String canRemoveYn) {
		this.canRemoveYn = canRemoveYn;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getTypeMessage() {
		return typeMessage;
	}
	public void setTypeMessage(String typeMessage) {
		this.typeMessage = typeMessage;
	}
	public String getCallBackYn() {
		return callBackYn;
	}
	public void setCallBackYn(String callBackYn) {
		this.callBackYn = callBackYn;
	}
	public String getCallBackUrl() {
		return callBackUrl;
	}
	public void setCallBackUrl(String callBackUrl) {
		this.callBackUrl = callBackUrl;
	}
}
