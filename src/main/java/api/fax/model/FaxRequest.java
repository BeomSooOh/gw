package api.fax.model;

import java.util.Map;

public class FaxRequest {
	private FaxRequestHeader header;
	
	public Map<String, Object> body; 

	public FaxRequestHeader getHeader() {
		return header;
	}

	public void setHeader(FaxRequestHeader header) {
		this.header = header;
	}

	public Map<String, Object> getBody() {
		return body;
	}

	public void setBody(Map<String, Object> body) {
		this.body = body;
	}
	
}
